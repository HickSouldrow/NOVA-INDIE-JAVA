 public void preencherTabela() {

        tblReqMinimos.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(3).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(4).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(5).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(6).setPreferredWidth(100);
        tblReqMinimos.getColumnModel().getColumn(7).setPreferredWidth(100);

        DefaultTableModel modelo = (DefaultTableModel) tblReqMinimos.getModel();
        modelo.setNumRows(1);

        try {
            con_loja_de_jogos.resultset.beforeFirst();
            while (con_loja_de_jogos.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_loja_de_jogos.resultset.getString("ReqMinId"), con_loja_de_jogos.resultset.getString("SOMin"), con_loja_de_jogos.resultset.getString("GPUMin"), con_loja_de_jogos.resultset.getString("CPUMin"), con_loja_de_jogos.resultset.getString("DirectXMin"), con_loja_de_jogos.resultset.getString("Armazenamento"),con_loja_de_jogos.resultset.getString("RAMmin"),con_loja_de_jogos.resultset.getString("OBS")
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! :\n " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void posicionarRegistro() {

        try {
            con_loja_de_jogos.resultset.first();
            mostrar_Dados();
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Não foi possível posicionar no primeiro registro: " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void mostrar_Dados() {

        try {
            con_loja_de_jogos.resultset.getString("ReqMinId");
            con_loja_de_jogos.resultset.getString("SOMin");
            con_loja_de_jogos.resultset.getString("GPUMin");
            con_loja_de_jogos.resultset.getString("CPUMin");
            con_loja_de_jogos.resultset.getString("DirectXMin");
            con_loja_de_jogos.resultset.getString("Armazenamento");
            con_loja_de_jogos.resultset.getString("RAMmin");
            con_loja_de_jogos.resultset.getString("OBS");

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Não localizou dados: " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }


