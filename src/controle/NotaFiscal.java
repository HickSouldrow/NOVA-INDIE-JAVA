private void consultarCliente() {
   String CodCliente = txtCodCliente.getText();
String NomeCliente = "";

try {
    // Certifique-se que a conexÃ£o (con_loja_de_jogos) estÃ¡ inicializada e ativa
    String clienteQuery = "SELECT Nome FROM Cliente WHERE CodCliente = ?";
    PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(clienteQuery);
    pstmt.setString(1, CodCliente);
    ResultSet rsCliente = pstmt.executeQuery();

    if (rsCliente.next()) {
        NomeCliente = rsCliente.getString("Nome");
        txtNomeCliente.setText(NomeCliente);
    } else {
        txtNomeCliente.setText("  N/A");
    }

    // Fechando o ResultSet e PreparedStatement para liberar recursos
    rsCliente.close();
    pstmt.close();

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Erro ao consultar cliente: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace(); // Para ajudar no diagnÃ³stico do erro
}
}
