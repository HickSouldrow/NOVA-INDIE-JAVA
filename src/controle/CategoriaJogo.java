    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
String CodCategoria = txtCodCategoria.getText();  // Obtenção manual do valor de CodCategoria
String CodJogo = txtCodJogo.getText();


try {

    
    String checkCategoriaQuery = "SELECT 1 FROM Categoria WHERE CodCategoria = ?";
    PreparedStatement pstmtCategoriaTipo = con_loja_de_jogos.prepareStatement(checkCategoriaQuery);
    pstmtCategoriaTipo.setString(1, CodCategoria);
    ResultSet rsCategoriaTipo = pstmtCategoriaTipo.executeQuery();

    if (!rsCategoriaTipo.next()) {
        
        JOptionPane.showMessageDialog(null, "Código de Categoria não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }

   
    String checkJogoQuery = "SELECT 1 FROM jogo WHERE CodJogo = ?";
    PreparedStatement pstmtJogo = con_loja_de_jogos.prepareStatement(checkJogoQuery);
    pstmtJogo.setString(1, CodJogo);
    ResultSet rsJogo = pstmtJogo.executeQuery();

    if (!rsJogo.next()) {
   
        JOptionPane.showMessageDialog(null, "Código do Jogo não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }    
    
    String insert_sql = "insert into CategoriaJogo (CodCategoria, CodJogo) values ('" + CodCategoria + "','" + CodJogo + "')";
    con_loja_de_jogos.statement.executeUpdate(insert_sql);

    JOptionPane.showMessageDialog(null, "Gravação realizada com sucesso", "Mensagem do programa", JOptionPane.INFORMATION_MESSAGE);

    // Atualizando a tabela após a inserção
    con_loja_de_jogos.executaSQL("select * from CategoriaJogo order by CodCategoria");
    preencherTabela();
    consultarCategoria();
    consultarJogo();
} catch (SQLException errosql) {
    JOptionPane.showMessageDialog(null, "\n Erro de gravação :\n" + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
}
    }//GEN-LAST:event_btnGravarActionPerformed

