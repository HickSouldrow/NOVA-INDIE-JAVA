private void carregarDados(String codCategoria, String codJogo) {
    try {
        String sql = "SELECT * FROM CategoriaJogo WHERE CodCategoria = '" + codCategoria + "' AND CodJogo = '" + codJogo + "'";
        ResultSet rs = con_loja_de_jogos.statement.executeQuery(sql);

        if (rs.next()) {
            txtCodCategoria.setText(rs.getString("CodCategoria"));
            txtCodJogo.setText(rs.getString("CodJogo"));

            valorAntigoCodCategoria = rs.getString("CodCategoria");
            valorAntigoCodJogo = rs.getString("CodJogo");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}
         
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    String sql = "";

    try {

        int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, 3);
        if (resposta == JOptionPane.YES_OPTION) {

            sql = "DELETE FROM CategoriaJogo WHERE CodJogo = ? AND CodCategoria = ?";
            PreparedStatement pstmtExcluir = con_loja_de_jogos.prepareStatement(sql);
            
            pstmtExcluir.setString(2, txtCodCategoria.getText());  
            pstmtExcluir.setString(1, txtCodJogo.getText());          
            
            int excluir = pstmtExcluir.executeUpdate();

            if (excluir > 0) {
                JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

                con_loja_de_jogos.executaSQL("SELECT * FROM CategoriaJogo ORDER BY CodCategoria");
                con_loja_de_jogos.resultset.first();
                preencherTabela();
                posicionarRegistro();
                consultarCategoria();
                consultarJogo();
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum registro encontrado para exclusão!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    } catch (SQLException excecao) {
        JOptionPane.showMessageDialog(null, "Erro na exclusão: " + excecao, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

