  public void preencherTabela() {

        tblCategoriaJogo.getColumnModel().getColumn(0).setPreferredWidth(90);
        tblCategoriaJogo.getColumnModel().getColumn(1).setPreferredWidth(110);

        DefaultTableModel modelo = (DefaultTableModel) tblCategoriaJogo.getModel();
        modelo.setNumRows(1);

        try {
            con_loja_de_jogos.resultset.beforeFirst();
            while (con_loja_de_jogos.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_loja_de_jogos.resultset.getString("CodCategoria"), con_loja_de_jogos.resultset.getString("CodJogo")
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! :\n " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

  
     private void consultarJogo() {
    String CodJogo = txtCodJogo.getText();
    String nomeJogo = "";

    try {
        
        String jogoQuery = "SELECT Nome FROM jogo WHERE CodJogo = ?";
        PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(jogoQuery);
        pstmt.setString(1, CodJogo);
        ResultSet rsJogo = pstmt.executeQuery();

        if (rsJogo.next()) {
            nomeJogo = rsJogo.getString("Nome");
            txtNomeJogo.setText(nomeJogo);
        } else {
            txtNomeJogo.setText("N/A");
        }
        
        rsJogo.close();
        pstmt.close();
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao consultar jogo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); 
    }
}

private void consultarCategoria() {
    String CodCategoria = txtCodCategoria.getText();
    String CategoriaTipo = "";

    try {

        String CategoriaQuery = "SELECT CategoriaTipo FROM Categoria WHERE CodCategoria = ?";
        PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(CategoriaQuery);
        pstmt.setString(1, CodCategoria);
        ResultSet rsCategoria = pstmt.executeQuery();
        
        if (rsCategoria.next()) {
            CategoriaTipo = rsCategoria.getString("CategoriaTipo");
            txtCategoriaTipo.setText(CategoriaTipo);

        } else {
            txtCategoriaTipo.setText("N/A");
        }
        
        rsCategoria.close();
        pstmt.close(); 
        
    }
    catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao consultar cliente: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); 
    }
}
  
