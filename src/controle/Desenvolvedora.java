    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String NomeDesenvolvedora = txtnomeDesenvolvedora.getText();
        String CNPJ = txtCNPJ.getText();
        String Email_Contato = txtEmail_Contato.getText();
        String SiteOficial = txtSiteOficial.getText();
        String sql;
        String msg = "";

        try {
            if (txtCodDesenvolvedora.getText().equals("")) {
                sql = "insert into Desenvolvedora (NomeDesenvolvedora, CNPJ, Email_Contato, SiteOficial) values ('" + NomeDesenvolvedora + "','" + CNPJ + "','" + Email_Contato + "','" + SiteOficial  + "')";
                msg = "Gravação de um novo registro";
            } else {
                sql = "update desenvolvedora set NomeDesenvolvedora='" + NomeDesenvolvedora + "', CNPJ='" + CNPJ + "', Email_Contato='" + Email_Contato + "', SiteOficial='" + SiteOficial + "' where CodDesenvolvedora=" + txtCodDesenvolvedora.getText();
                msg = "Alteração de registro";
            }
            con_loja_de_jogos.statement.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, msg + " realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

            con_loja_de_jogos.executaSQL("select * from Desenvolvedora order by CodDesenvolvedora");
            preencherTabela();

        } catch (SQLException errosql) {
            JOptionPane.showMessageDialog(null, "\n Erro na gravação \n" + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String sql = "";

        try {
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro: ", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, 3);
            if (resposta == 0) {
                sql = "delete from Desenvolvedora where CodDesenvolvedora =" + txtCodDesenvolvedora.getText();
                int excluir = con_loja_de_jogos.statement.executeUpdate(sql);

                if (excluir == 1) {
                    JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                    con_loja_de_jogos.executaSQL("select * from Desenvolvedora order by CodDesenvolvedora");
                    con_loja_de_jogos.resultset.first();
                    preencherTabela();
                    posicionarRegistro();
                } else {
                    JOptionPane.showMessageDialog(null, "Operação cancelada pelo usuário!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } catch (SQLException excecao) {
            JOptionPane.showMessageDialog(null, "Erro na exclusão: " + excecao, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

