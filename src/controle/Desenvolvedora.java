  public void preencherTabela() {

        tblDesenvolvedora.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblDesenvolvedora.getColumnModel().getColumn(1).setPreferredWidth(110);
        tblDesenvolvedora.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblDesenvolvedora.getColumnModel().getColumn(3).setPreferredWidth(100);
        tblDesenvolvedora.getColumnModel().getColumn(4).setPreferredWidth(100);
        

        DefaultTableModel modelo = (DefaultTableModel) tblDesenvolvedora.getModel();
        modelo.setNumRows(0);

        try {
            con_loja_de_jogos.resultset.beforeFirst();
            while (con_loja_de_jogos.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_loja_de_jogos.resultset.getString("CodDesenvolvedora"), con_loja_de_jogos.resultset.getString("NomeDesenvolvedora"), con_loja_de_jogos.resultset.getString("CNPJ"), con_loja_de_jogos.resultset.getString("Email_Contato"), con_loja_de_jogos.resultset.getString("SiteOficial")
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! :\n " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

