   private void consultarFaixaEtaria() {
    String CodFaixaEtaria = txtCodFaixaEtaria.getText();
    String ClassificacaoIndicativa = "";

    try {
        // Certifique-se que a conexão (con_loja_de_FaixaEtarias) está inicializada e ativa
        String FaixaEtariaQuery = "SELECT ClassificacaoIndicativa FROM ClassificacaoIndicativa WHERE CodFaixaEtaria = ?";
        PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(FaixaEtariaQuery);
        pstmt.setString(1, CodFaixaEtaria);
        ResultSet rsFaixaEtaria = pstmt.executeQuery();
        
        if (rsFaixaEtaria.next()) {
            ClassificacaoIndicativa = rsFaixaEtaria.getString("ClassificacaoIndicativa");
            txtFaixaEtaria.setText(ClassificacaoIndicativa);
        } else {
            txtFaixaEtaria.setText("   N/A");
        }
        
        rsFaixaEtaria.close();
        pstmt.close(); // Lembre-se de fechar os recursos
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao consultar jogo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Para ajudar no diagnóstico do erro
    }
}

private void consultarOBS() {
    String ReqMinId = txtReqMinId.getText();
    String OBS = "";

    try {
        // Certifique-se que a conexão (con_loja_de_jogos) está inicializada e ativa
        String OBSQuery = "SELECT OBS FROM ReqMinimos WHERE ReqMinId = ?";
        PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(OBSQuery);
        pstmt.setString(1, ReqMinId);
        ResultSet rsOBS = pstmt.executeQuery();
        
        if (rsOBS.next()) {
            OBS = rsOBS.getString("OBS");
            txtOBS.setText(OBS);
        } else {
            txtOBS.setText("N/A");
        }
        
        rsOBS.close();
        pstmt.close(); // Lembre-se de fechar os recursos
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao consultar OBS: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Para ajudar no diagnóstico do erro
    }
}
