    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
                String nome = txtnome.getText();
                String dtLancamento = txtDtLancamento.getText();
                String avaliacao = txtAvaliacao.getText();
                String CodFaixaEtaria = txtCodFaixaEtaria.getText();
                String ReqMinId = txtReqMinId.getText();
                String Preco = txtPreco.getText();
                String sql;
                String msg = "";

                try {
                    
             
                        String checkFaixaEtariaQuery = "SELECT 1 FROM ClassificacaoIndicativa WHERE CodFaixaEtaria = ?";
                        PreparedStatement pstmtFaixaEtaria = con_loja_de_jogos.prepareStatement(checkFaixaEtariaQuery);
                        pstmtFaixaEtaria.setString(1, CodFaixaEtaria);
                        ResultSet rsClassificacaoIndicativa = pstmtFaixaEtaria.executeQuery();

    if (!rsClassificacaoIndicativa.next()) {
        JOptionPane.showMessageDialog(null, "Classificação não encontrada!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }
    
                        String checkOBSQuery = "SELECT 6 FROM ReqMinimos WHERE ReqMinId = ?";
                        PreparedStatement pstmtOBS = con_loja_de_jogos.prepareStatement(checkOBSQuery);
                        pstmtOBS.setString(1, ReqMinId);
                        ResultSet rsOBS = pstmtOBS.executeQuery();

    if (!rsOBS.next()) {
        JOptionPane.showMessageDialog(null, "Observações não encontradas!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }

                    if (txtCodJogo.getText().equals("")) {
                        sql = "insert into jogo (nome, dtLancamento, avaliacao, CodFaixaEtaria, ReqMinId, Preco) values ('" + nome + "','" + dtLancamento + "','" + avaliacao + "','" + CodFaixaEtaria + "','" + ReqMinId  + "','" + Preco  + "')";
                        msg = "Gravação de um novo registro";
                    } else {
                        sql = "update jogo set nome='" + nome + "', DtLancamento='" +dtLancamento + "', avaliacao='" + avaliacao + "', CodFaixaEtaria='" + CodFaixaEtaria + "', ReqMinId='" + ReqMinId + "', Preco='" + Preco + "' where CodJogo=" + txtCodJogo.getText();
                        msg = "Alteração de registro";
                    }
                    con_loja_de_jogos.statement.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, msg + " realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

                    con_loja_de_jogos.executaSQL("select * from jogo order by CodJogo");
                    preencherTabela();

                } catch (SQLException errosql) {
                    JOptionPane.showMessageDialog(null, "\n Erro na gravação \n" + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
                String sql = "";

                try {
                    int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro: ", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, 3);
                    if (resposta == 0) {
                        sql = "delete from jogo where CodJogo =" + txtCodJogo.getText();
                        int excluir = con_loja_de_jogos.statement.executeUpdate(sql);

                        if (excluir == 1) {
                            JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                            con_loja_de_jogos.executaSQL("select * from jogo order by CodJogo");
                            con_loja_de_jogos.resultset.first();
                            preencherTabela();
                            posicionarRegistro();
                            consultarFaixaEtaria();
                            consultarOBS();
                        } else {
                            JOptionPane.showMessageDialog(null, "Operação cancelada pelo usuário!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }

                } catch (SQLException excecao) {
                    JOptionPane.showMessageDialog(null, "Erro na exclusão: " + excecao, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                }
    }//GEN-LAST:event_btnExcluirActionPerformed

