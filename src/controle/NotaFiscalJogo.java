  public void preencherTabela() {

        tblNotaFiscalJogo.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblNotaFiscalJogo.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblNotaFiscalJogo.getColumnModel().getColumn(2).setPreferredWidth(100);

        DefaultTableModel modelo = (DefaultTableModel) tblNotaFiscalJogo.getModel();
        modelo.setNumRows(1);

        try {
            con_loja_de_jogos.resultset.beforeFirst();
            while (con_loja_de_jogos.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_loja_de_jogos.resultset.getString("CodNotaFiscal"), con_loja_de_jogos.resultset.getString("CodJogo"), con_loja_de_jogos.resultset.getString("QtdVend")
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! :\n " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

