    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
      String CodNotaFiscal = txtCodNotaFiscal.getText();
      String CodJogo = txtCodJogo.getText();
      String QtdVend = txtQtdVend.getText();

try {

    String checkNotaFiscalQuery = "SELECT 1 FROM NotaFiscal WHERE CodNotaFiscal = ?";
    PreparedStatement pstmtNotaFiscal = con_loja_de_jogos.prepareStatement(checkNotaFiscalQuery);
    pstmtNotaFiscal.setString(1, CodNotaFiscal);
    ResultSet rsNotaFiscal = pstmtNotaFiscal.executeQuery();

    if (!rsNotaFiscal.next()) {

        JOptionPane.showMessageDialog(null, "Código de Nota Fiscal não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return; 
    }


    String checkJogoQuery = "SELECT 1 FROM jogo WHERE CodJogo = ?";
    PreparedStatement pstmtJogo = con_loja_de_jogos.prepareStatement(checkJogoQuery);
    pstmtJogo.setString(1, CodJogo);
    ResultSet rsJogo = pstmtJogo.executeQuery();

    if (!rsJogo.next()) {

        JOptionPane.showMessageDialog(null, "Código do Jogo não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return; 
    }


    String insert_sql = "INSERT INTO NotaFiscalJogo (CodNotaFiscal, CodJogo, QtdVend) VALUES (?, ?, ?)";
    PreparedStatement pstmtInsert = con_loja_de_jogos.prepareStatement(insert_sql);
    pstmtInsert.setString(1, CodNotaFiscal);
    pstmtInsert.setString(2, CodJogo);
    pstmtInsert.setString(3, QtdVend);
    pstmtInsert.executeUpdate();

    JOptionPane.showMessageDialog(null, "Gravação realizada com sucesso", "Mensagem do programa", JOptionPane.INFORMATION_MESSAGE);


    con_loja_de_jogos.executaSQL("SELECT * FROM NotaFiscalJogo ORDER BY CodNotaFiscal");
    preencherTabela();
    consultarCliente();
    consultarJogo();

} catch (SQLException errosql) {
    JOptionPane.showMessageDialog(null, "\n Erro de gravação:\n" + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
}


    }//GEN-LAST:event_btnGravarActionPerformed

