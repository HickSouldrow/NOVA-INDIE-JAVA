    private void consultarJogo() {
    String CodJogo = txtCodJogo.getText();
    String nomeJogo = "";

    try {
        
        String jogoQuery = "SELECT Nome FROM jogo WHERE CodJogo = ?";
        PreparedStatement pstmt = con_loja_de_jogos.prepareStatement(jogoQuery);
        pstmt.setString(1, CodJogo);
        ResultSet rsJogo = pstmt.executeQuery();

        if (rsJogo.next()) {
            nomeJogo = rsJogo.getString("Nome");
            txtNomeJogo.setText(nomeJogo);
        } else {
            txtNomeJogo.setText("N/A");
        }
        
        rsJogo.close();
        pstmt.close();
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erro ao consultar jogo: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); 
    }
}

