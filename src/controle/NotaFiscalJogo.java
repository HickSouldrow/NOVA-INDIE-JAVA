    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
String CodJogo = txtCodJogo.getText();
String CodNotaFiscal = txtCodNotaFiscal.getText();  
String QtdVend = txtQtdVend.getText();  

String sql;
String msg = "";

try {

    String checkNotaFiscalQuery = "SELECT 1 FROM NotaFiscal WHERE CodNotaFiscal = ?";
    PreparedStatement pstmtNotaFiscal = con_loja_de_jogos.prepareStatement(checkNotaFiscalQuery);
    pstmtNotaFiscal.setString(1, CodNotaFiscal);
    ResultSet rsNotaFiscal = pstmtNotaFiscal.executeQuery();

    if (!rsNotaFiscal.next()) {
        JOptionPane.showMessageDialog(null, "Código de Nota Fiscal não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }


    String checkJogoQuery = "SELECT 1 FROM jogo WHERE CodJogo = ?";
    PreparedStatement pstmtJogo = con_loja_de_jogos.prepareStatement(checkJogoQuery);
    pstmtJogo.setString(1, CodJogo);
    ResultSet rsJogo = pstmtJogo.executeQuery();

    if (!rsJogo.next()) {
        JOptionPane.showMessageDialog(null, "Código do Jogo não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;  
    }


    sql = "UPDATE NotaFiscalJogo SET QtdVend = ? WHERE CodNotaFiscal = ? AND CodJogo = ?";
    PreparedStatement pstmtUpdate = con_loja_de_jogos.prepareStatement(sql);
    pstmtUpdate.setString(1, QtdVend);
    pstmtUpdate.setString(2, CodNotaFiscal);
    pstmtUpdate.setString(3, CodJogo);

    int rowsAffected = pstmtUpdate.executeUpdate();

    if (rowsAffected == 0) {
        JOptionPane.showMessageDialog(null, "Nenhum registro encontrado para alterar!", "Erro", JOptionPane.ERROR_MESSAGE);
        
    } else {
        JOptionPane.showMessageDialog(null, "Alteração realizada com sucesso!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

        con_loja_de_jogos.executaSQL("SELECT * FROM NotaFiscalJogo ORDER BY CodNotaFiscal");
        preencherTabela();
        consultarCliente();
        consultarJogo();
    }

} catch (SQLException errosql) {
    JOptionPane.showMessageDialog(null, "\nErro na gravação:\n" + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
}

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
                                        
    String sql = "";

    try {

        int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, 3);
        if (resposta == JOptionPane.YES_OPTION) {

            sql = "DELETE FROM NotaFiscalJogo WHERE CodNotaFiscal = ? AND CodJogo = ? AND QtdVend = ?";
            PreparedStatement pstmtExcluir = con_loja_de_jogos.prepareStatement(sql);
            
            pstmtExcluir.setString(1, txtCodNotaFiscal.getText());  
            pstmtExcluir.setString(2, txtCodJogo.getText());      
            pstmtExcluir.setString(3, txtQtdVend.getText());     
            
            int excluir = pstmtExcluir.executeUpdate();

            if (excluir > 0) {
                JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

                con_loja_de_jogos.executaSQL("SELECT * FROM NotaFiscalJogo ORDER BY CodNotaFiscal");
                con_loja_de_jogos.resultset.first();
                preencherTabela();
                posicionarRegistro();
                consultarCliente();
                consultarJogo();
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum registro encontrado para exclusão!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    } catch (SQLException excecao) {
        JOptionPane.showMessageDialog(null, "Erro na exclusão: " + excecao, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_btnExcluirActionPerformed

